    private Connection con;
    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (validarControles()) {
            ResultSet rs = null;                       
            PreparedStatement st = null;  
            try {
                con = Conexion.Conexion.conectar();
                Usuario u = new Usuario(txtUsuario.getText(), txtClave.getText(), cmbEstado.getSelectedItem().toString(), cmbRol.getSelectedItem().toString());
                st = con.prepareStatement("select * from usuarios where usuario = ?");
                st.setString(1,u.getUsuario());
                rs = st.executeQuery();
                
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this,
                    "Ya existe una persona con ese usuario, los datos se remplazarán",
                    "Ingresar Nuevo Usuario",
                    JOptionPane.ERROR_MESSAGE);
                    
                    st = con.prepareStatement("update usuarios set clave = ?, estado =?, rol = ? where usuario =?");                      
                    st.setString(1, u.getClave());
                    st.setString(2, u.getEstado());
                    st.setString(3, u.getRol());
                    st.setString(4,u.getUsuario());
                }else{                
                    st = con.prepareStatement("insert into usuarios (usuario,clave,estado,rol) values (?,?,?,?)");                 
                    st.setString(1,u.getUsuario());
                    st.setString(2, u.getClave());
                    st.setString(3, u.getEstado());
                    st.setString(4, u.getRol());

                    st.executeUpdate();
                }
                
                JOptionPane.showMessageDialog(this,
                    "Usuario ingresado con éxito",
                    "Ingresar Nuevo Usuario",
                    JOptionPane.ERROR_MESSAGE);
                
                dispose();
                
                System.out.println("Usuario ingresado con éxito");
            } catch (Exception ex) {
            } finally{
                if ( con!=null) {
                    try {
                        con.close();
                    } catch (SQLException ex) {
                        Logger.getLogger(FrmVehiculo.class.getName()).log(Level.SEVERE, null, ex);
                    }
            }
            if (st!=null) {
                try{
                    st.close();
                }catch (SQLException ex) {
                   Logger.getLogger(FrmVehiculo.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (rs!= null) {
                try{
                    rs.close();
                }catch (SQLException ex) {
                    Logger.getLogger(FrmVehiculo.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

