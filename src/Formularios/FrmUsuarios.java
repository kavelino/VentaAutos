    //se valida que los controles no esten vacios
    private boolean validarControles() {
        if (txtUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe Ingresar un usuario válido",
                    "Ingresar Nuevo Usuario",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (txtClave.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe Ingresar una clave válida",
                    "Ingresar Nuevo Usuario",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (cmbEstado.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe Ingresar algún estado",
                    "Ingresar Nuevo Usuario",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (cmbRol.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe Ingresar algún rol",
                    "Ingresar Nuevo Usuario",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }
